    /**
     * action when search button is clicked
     * @param evt 
     */
    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        
        String MyKadNumber = MyKadNoTF.getText();
        if(MyKadNumber.equals(""))
        {
            JOptionPane.showMessageDialog(this, "MyKad Number can not be empty!");
        }
        
        if(!MyKadNumber.equals(""))
        {
            if(agn.findChild(MyKadNumber) == null)
            {
                JOptionPane.showMessageDialog(this, "Child is not found. Please record the child!");
                ChildNameTF.setEnabled(true);
                ChildAddressTF.setEnabled(true);
                DateOfBirth.setEnabled(true);
                OKBtn.setEnabled(true);
                searchBtn.setEnabled(false);
                MyKadNoTF.setEnabled(false);
            }else{
                ChildNameTF.setText(agn.findChild(MyKadNumber).getChildName());
                ChildAddressTF.setText(agn.findChild(MyKadNumber).getHomeAddress());
                Date date = Date.from(agn.findChild(MyKadNumber).getDateOfBirth().atStartOfDay(ZoneId.systemDefault()).toInstant());
                DateOfBirth.setDate(date);
                ChildNameTF.setEnabled(true);
                ChildAddressTF.setEnabled(true);
                DateOfBirth.setEnabled(true);
                OKBtn.setEnabled(true);
                searchBtn.setEnabled(false);
                MyKadNoTF.setEnabled(false);
        }
        }
        
    }//GEN-LAST:event_searchBtnActionPerformed


