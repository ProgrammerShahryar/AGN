    /**
     * action when Ok button is clicked with validation
     * @param evt 
     */
    private void OKBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OKBtnActionPerformed
        // TODO add your handling code here:
       
        try 
        {
            boolean valid = false;
            
            String MyKadNumber = MyKadNoTF.getText();
            String name = ChildNameTF.getText();
            String address = ChildAddressTF.getText();
            LocalDate dateOfBirth = DateOfBirth.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
            
            int age = LocalDate.now().getYear() - dateOfBirth.getYear();
       
            if (name.equals(""))
                JOptionPane.showMessageDialog(this, "Name can not be blank!");
            else if (address.equals(""))
                JOptionPane.showMessageDialog(this, "Home Address can not be blank!");
            else if (dateOfBirth.isAfter(LocalDate.now()))
                JOptionPane.showMessageDialog(this, "You can not record a child that was not born yet!");
            else if (age > 18)
                JOptionPane.showMessageDialog(this, "Age can not be above 18!");
            else
                valid = true;
            
            if(valid)
            {
                if(agn.findChild(MyKadNumber) != null)
                {
                    agn.findChild(MyKadNumber).setChildName(name);
                    agn.findChild(MyKadNumber).setDateOfBirth(dateOfBirth);
                    agn.findChild(MyKadNumber).setHomeAddress(address);
                    JOptionPane.showMessageDialog(this, name+" with MyKad number:"+MyKadNumber+" updated successfully");
                }
                else
                {
                    agn.addChild(name, MyKadNumber, address, dateOfBirth);
                    JOptionPane.showMessageDialog(this, name+" with MyKad number:"+MyKadNumber+" added successfully");
                }
                
                this.dispose();    
            }
        } 
        catch (NullPointerException npe) 
        {
            JOptionPane.showMessageDialog(this, "Please, set the date of birth!");
        }
        
    }//GEN-LAST:event_OKBtnActionPerformed

